#!/usr/bin/env python3


fst_num = int(input("Give me your first number: "))
sec_num = int(input("Give me your second number: "))
print(f"Thank You!")

Add_sum = fst_num + sec_num
Min_sum = fst_num - sec_num
Mult_sum = fst_num * sec_num
Div_sum = fst_num / sec_num


print(f"{fst_num} + {sec_num} = {Add_sum}")
print(f"{fst_num} - {sec_num} = {Min_sum}")
print(f"{fst_num} * {sec_num} = {Mult_sum}")
print(f"{fst_num} / {sec_num} = {Div_sum}")